"""
    simulate_fit(
        genomes::Genomes,
        phenomes::Phenomes;
        fname_fit_jld2::String="simulated_fit.jld2",
        overwrite::Bool=true,
    )::Fit

Generate a simulated genomic prediction model from compatible genomic and phenomic
datasets and export the result as a JLD2 file.

The function constructs a synthetic `Fit` object using genotype and phenotype
information from the supplied `Genomes` and `Phenomes` objects. Marker effects are
generated randomly, predictions are calculated from allele frequencies, and summary
performance metrics are computed from the observed and predicted phenotypic values.

The resulting model is written to disk and returned for subsequent analysis or
testing.

# Arguments

- `genomes::Genomes`: Genomic dataset containing marker information and allele
  frequencies.
- `phenomes::Phenomes`: Phenomic dataset containing trait measurements for the same
  set of entries.
- `fname_fit_jld2::String="simulated_fit.jld2"`: Path to the output JLD2 file.
- `overwrite::Bool=true`: If `true`, overwrite an existing output file.

# Returns

- `Fit`: Simulated genomic prediction model.

# Throws

- `ErrorException`: If the entries in `Genomes` and `Phenomes` do not align
  exactly.

# Notes

- Genomic and phenomic datasets must contain the same entries in the same order.
- Missing allele frequencies and phenotype values are replaced with `0.0` prior to
  model construction.
- The simulated model is labelled as `"OLS"`.
- Marker effects are generated randomly and are not estimated from the data.
- Predicted values are calculated as the matrix product of allele frequencies and
  simulated marker effects.
- Model performance metrics include correlation (`cor`), root mean squared error
  (`rmse`), and coefficient of determination (`r2`).
- The resulting fit is exported using `GenomicBreedingIO.writejld2`.

# Examples

```jldoctest; setup=:(using GenomicBreedingCore, GenomicBreedingIO, GenomicBreedingDB, DataFrames, CSV, StatsBase, LibPQ, Dates)
julia> genomes = simulate_genomes();

julia> phenomes = simulate_trials(genomes) |> simulate_phenomes;

julia> fit = simulate_fit(genomes, phenomes);

julia> checkdims(fit)
true

julia> isfile("simulated_fit.jld2")
true
```
"""
function simulate_fit(
    genomes::Genomes,
    phenomes::Phenomes;
    fname_fit_jld2::String = "simulated_fit.jld2",
    overwrite::Bool = true,
)::Fit
    # genomes = simulate_genomes(); trials = simulate_trials(genomes); phenomes = simulate_phenomes(trials); fname_fit_jld2::String = "simulated_fit.jld2"; overwrite = true; genomes = genomes; phenomes = sim_phenomes["phenomes"]
    if genomes.entries != phenomes.entries
        error("The Genomes and Phenomes are not compatible, i.e. the entries do not align perfectly!")
    end
    X = genomes.allele_frequencies
    X[ismissing.(X)] .= 0.0
    y = phenomes.phenotypes[:, 1]
    y[ismissing.(y)] .= 0.0
    fit = Fit(n = length(genomes.entries), l = length(genomes.loci_alleles))
    fit.model = "OLS"
    fit.b_hat_labels = genomes.loci_alleles
    fit.b_hat = rand(length(genomes.loci_alleles))
    fit.trait = phenomes.traits[1]
    fit.entries = phenomes.entries
    fit.populations = phenomes.populations
    fit.y_true = y
    fit.y_pred = X * fit.b_hat
    fit.metrics = Dict(
        "cor" => cor(fit.y_true, fit.y_pred),
        "rmse" => mean(sqrt.((fit.y_true - fit.y_pred) .^ 2)),
        "r2" => 1.00 - (var(fit.y_true - fit.y_pred) / var(fit.y_true)),
    )
    GenomicBreedingIO.writejld2(fit, fname = fname_fit_jld2, overwrite = overwrite)
    fit
end
