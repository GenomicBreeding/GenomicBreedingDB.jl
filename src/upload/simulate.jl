"""
    simulate_fit(
        genomes::Genomes,
        phenomes::Phenomes;
        fname_fit::String="simulated_fit.jld2",
        overwrite::Bool=true,
    )::Fit

Generate a simulated genomic prediction model fit from compatible genomic and
phenomic datasets.

This function constructs a synthetic `Fit` object by combining genotype data
from a `Genomes` object with phenotype data from a `Phenomes` object. Marker
effects are randomly generated, genomic estimated values are calculated, model
performance metrics are computed, and the resulting fit is written to a JLD2
file.

The generated fit is intended for testing, examples, benchmarking, and
workflow development rather than statistical inference.

# Arguments

- `genomes::Genomes`:
  A valid genomic dataset containing marker information and allele
  frequencies.
- `phenomes::Phenomes`:
  A valid phenomic dataset containing phenotypic observations corresponding
  to the same entries represented in `genomes`.

# Keyword Arguments

- `fname_fit::String="simulated_fit.jld2"`:
  Output JLD2 filename for the simulated fit.
- `overwrite::Bool=true`:
  Overwrite an existing output file if present.

# Returns

- `"fit"`:
  The simulated `Fit` object.

# Throws

- `ErrorException` if the entries contained in `genomes` and `phenomes`
  do not align exactly.

# Notes

- Missing allele frequencies are replaced with `0.0` before fitting.
- Missing phenotype values are replaced with `0.0`.
- Marker effects (`b_hat`) are randomly sampled and do not represent true
  genetic effects.
- The model is assigned `"OLS"` as its model label, although no ordinary
  least-squares fitting is actually performed.
- Performance metrics are calculated from the simulated predictions and
  should not be interpreted scientifically.

The following metrics are stored in `fit.metrics`:

- `"cor"`: Pearson correlation between observed and predicted values.
- `"rmse"`: Root mean squared error.
- `"r2"`: Coefficient of determination.

# Example

```jldoctest; setup=:(using GenomicBreedingCore, GenomicBreedingIO, GenomicBreedingDB, DataFrames, CSV, StatsBase, LibPQ, Dates)
julia> genomes = simulate_genomes();

julia> phenomes = simulate_trials(genomes) |> simulate_phenomes;

julia> fit = simulate_fit(genomes, phenomes);

julia> checkdims(fit)
true

julia> isfile("simulated_fit.jld2")
true
```
"""
function simulate_fit(
    genomes::Genomes,
    phenomes::Phenomes;
    fname_fit::String = "simulated_fit.jld2",
    overwrite::Bool = true,
)::Fit
    # genomes = simulate_genomes(); trials = simulate_trials(genomes); phenomes = simulate_phenomes(trials); fname_fit::String = "simulated_fit.jld2"; overwrite = true; genomes = genomes; phenomes = sim_phenomes["phenomes"]
    if genomes.entries != phenomes.entries
        error("The Genomes and Phenomes are not compatible, i.e. the entries do not align perfectly!")
    end
    X = genomes.allele_frequencies
    X[ismissing.(X)] .= 0.0
    y = phenomes.phenotypes[:, 1]
    y[ismissing.(y)] .= 0.0
    fit = Fit(n = length(genomes.entries), l = length(genomes.loci_alleles))
    fit.model = "OLS"
    fit.b_hat_labels = genomes.loci_alleles
    fit.b_hat = rand(length(genomes.loci_alleles))
    fit.trait = phenomes.traits[1]
    fit.entries = phenomes.entries
    fit.populations = phenomes.populations
    fit.y_true = y
    fit.y_pred = X * fit.b_hat
    fit.metrics = Dict(
        "cor" => cor(fit.y_true, fit.y_pred),
        "rmse" => mean(sqrt.((fit.y_true - fit.y_pred) .^ 2)),
        "r2" => 1.00 - (var(fit.y_true - fit.y_pred) / var(fit.y_true)),
    )
    GenomicBreedingIO.writejld2(fit, fname = fname_fit, overwrite = overwrite)
    fit
end
