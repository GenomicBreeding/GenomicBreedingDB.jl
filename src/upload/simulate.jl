    if isfile(fname_reference_genome) && !overwrite
        error("The \"$fname_reference_genome\" file exists and overwrite is set to false!")
    end
    if isfile(fname_reference_genome) && overwrite
        rm(fname_reference_genome)
    end
    idx_fin = vcat(sort(sample(1:(n_bases-1), n_chromosomes-1, replace = false)), n_bases)
    idx_ini = vcat([1], idx_fin[1:(end-1)] .+ 1)
    open(fname_reference_genome, "w") do io
        # io = open(fname_reference_genome, "w")
        for i = 1:n_chromosomes
            # i = 1
            n = idx_fin[i] - (idx_ini[i]-1)
            seq = sample(['A', 'T', 'C', 'G'], n)
            counter = 0
            println(io, ">chrom$i")
            for s in seq
                # s = seq[1]
                if counter < max_characters_per_line
                    counter += 1
                    write(io, s)
                else
                    counter = 0
                    write(io, "\n$s")
                end
            end
            write(io, '\n')
        end
        # close(io)
    end
    nothing
end

