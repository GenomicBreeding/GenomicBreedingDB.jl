Generate a simulated reference genome in FASTA format.

The genome is composed of a specified number of chromosomes whose combined length
equals `n_bases`. Chromosome sequences are generated by randomly sampling the DNA
bases `A`, `T`, `C`, and `G`. Each chromosome is written as a separate FASTA record
with a header of the form `>chromN`.

Sequence lines are wrapped according to `max_characters_per_line` to produce a
standard FASTA-formatted output file.

# Arguments

- `fname_reference_genome::String="simulated_reference_genome.fa"`: Path to the
  output FASTA file.
- `n_bases::Int64=10_000`: Total number of bases to generate across all
  chromosomes.
- `n_chromosomes::Int64=7`: Number of chromosomes to simulate.
- `max_characters_per_line::Int64=80`: Maximum number of sequence characters per
  FASTA line.
- `overwrite::Bool=true`: If `true`, replace an existing output file.

# Returns

- `Nothing`: The simulated genome is written to disk.

# Throws

- `ErrorException`: If the output file already exists and `overwrite` is `false`.

# Notes

- Chromosome lengths are generated by partitioning `n_bases` into
  `n_chromosomes` intervals.
- Nucleotide bases are sampled uniformly from `['A', 'T', 'C', 'G']`.
- Existing output files are removed before writing when `overwrite=true`.
- FASTA headers are written in the format `>chrom1`, `>chrom2`, ..., `>chromN`.
- The function writes directly to disk and does not return the generated
  sequences.

# Examples

