"""
    simulate_environments(
        trials::Trials;
        fname_output::String="simulated_environments.tsv",
        n_measurements_in_between_phenotypings::Int64=10,
        sparsity::Float64=0.05,
        overwrite::Bool=true,
        verbose::Bool=false,
    )::DataFrame

Simulate environmental measurements for a trial dataset.

This function generates synthetic environmental covariates from a `Trials`
object. The trial observations are first converted to a tabular
representation and validated. Unique phenotyping dates are extracted from the
`measurements` column and environmental measurement dates are generated either:

- Daily for a specified duration when only a single phenotyping date exists.
- At approximately regular intervals between consecutive phenotyping dates
  when multiple phenotyping dates exist.

Environmental measurements are then simulated for each unique combination of
measurement date and trial identifiers (`sites`, `experiments`, and
`treatments`, when present).

The following environmental variables are generated:

- `rainfall_mm_per_day`
- `solar_radiation_MJ_per_m2_per_day`
- `minimum_temperature_C_per_day`
- `maximum_temperature_C_per_day`
- `humidity_perc_per_day`

Values are sampled independently from predefined ranges and are intended for
testing, examples, tutorials, and workflow development.

# Arguments

- `trials::Trials`:
  A valid `Trials` object containing a `measurements` field with one or more
  phenotyping dates.

# Keyword Arguments

- `fname_output::String="simulated_environments.tsv"`:
  Output tab-delimited file containing the simulated environmental data.
- `n_measurements_in_between_phenotypings::Int64=10`:
  Number of environmental measurement intervals generated between consecutive
  phenotyping dates. If only a single phenotyping date is present,
  measurements are generated daily for this many days following that date.
- `sparsity::Float64=0.05`:
  Proportion of simulated environmental observations to replace with
  `missing`.
- `overwrite::Bool=true`:
  Overwrite an existing output file.
- `verbose::Bool=false`:
  Display progress information during simulation.

# Returns
- `"df_environments"`:
  DataFrame containing the simulated environmental data.

# Throws

- `ErrorException` if the trial dataset is invalid.
- `ErrorException` if the required `measurements` column is absent.

# Notes

- Environmental variables are simulated independently and therefore do not
  exhibit realistic meteorological correlations.
- Generated values should not be used for scientific inference.
- The output is intended primarily for software testing, demonstrations, and
  benchmarking workflows.

# Example

```jldoctest; setup=:(using GenomicBreedingCore, GenomicBreedingIO, GenomicBreedingDB, DataFrames, CSV, StatsBase, LibPQ, Dates)
julia> df_environments = simulate_genomes() |> simulate_trials |> simulate_environments;

julia> prod(size(df_environments)) > 0
true

julia> isfile("simulated_environments.tsv")
true
```
"""
function simulate_environments(
    trials::Trials;
    fname_environments::String = "simulated_environments.tsv",
    n_measurements_in_between_phenotypings::Int64 = 10,
    sparsity::Float64 = 0.05,
    overwrite::Bool = true,
    verbose::Bool = false,
)::DataFrame
    # genomes = simulate_genomes(); trials = simulate_trials(genomes); trials = trials
    # fname_environments::String = "simulated_environments.tsv"; overwrite::Bool = true; verbose::Bool = false; n_measurements_in_between_phenotypings::Int64 = 10; sparsity=0.05
    if isfile(fname_environments) && !overwrite
        error("The \"$fname_environments\" file exists and overwrite is set to false!")
    end
    if isfile(fname_environments) && overwrite
        rm(fname_environments)
    end
    df_trial = tabularise(trials)
    validate_trials(df_trial)
    if "measurements"∉names(df_trial)
        error("Missing required measurement dates!")
    end
    measurements = sort([String("$x") for x in unique(df_trial.measurements)])
    validate_date.(measurements)
    # Define multiple measurement days
    measurement_dates = String[]
    if length(measurements) == 1
        for x in measurements
            # x = measurements[1]
            d = Date(x, dateformat"yyyy-mm-dd")
            append!(
                measurement_dates,
                [String("$x") for x in collect(d:Day(1):(d+Day(n_measurements_in_between_phenotypings)))],
            )
        end
    else
        for i = 2:length(measurements)
            # i = 2
            d_ini = Date(measurements[i-1], dateformat"yyyy-mm-dd")
            d_fin = Date(measurements[i], dateformat"yyyy-mm-dd")
            d_step = Day(ceil((d_fin - d_ini) / Day(n_measurements_in_between_phenotypings)))
            append!(measurement_dates, [String("$x") for x in collect(d_ini:d_step:d_fin)])
        end
    end
    measurement_dates = unique(measurement_dates)
    # Find all unique combinatons of the id columns except measurements from the input trial dataframe
    existing_id_columns = names(df_trial) ∩ ["sites", "experiments", "treatments"] # except measurements
    ids_concat_tmp = String[]
    for id in existing_id_columns
        # id = existing_id_columns[1]
        ids_concat_tmp = if length(ids_concat_tmp) == 0
            String.(df_trial[!, id])
        else
            String.(string.(ids_concat_tmp, "|||", df_trial[!, id]))
        end
    end
    ids_concat_tmp = unique(ids_concat_tmp)
    # Include the measurement dates
    ids_concat = String[]
    for m in measurement_dates
        # m = measurement_dates[1]
        append!(ids_concat, string.(m, "|||", ids_concat_tmp))
    end
    ids_concat = unique(ids_concat)
    ids_concat_split = split.(ids_concat, "|||")
    existing_id_columns = vcat(["measurements"], existing_id_columns)
    # Instantiate the output dataframe using the unique id combinations
    df_environments = DataFrame()
    for j = 1:length(existing_id_columns)
        # j = 1
        df_environments[!, existing_id_columns[j]] = [String(x[j]) for x in ids_concat_split]
    end
    # Simulate roughly environment data
    environment_variables = Dict(
        "rainfall_mm_per_day" => collect(0.0:123.0),
        "solar_radiation_MJ_per_m2_per_day" => collect(3.0:28.0),
        "minimum_temperature_C_per_day" => collect(-2.0:32.0),
        "maximum_temperature_C_per_day" => collect(6.0:47.0),
        "humidity_perc_per_day" => collect(15.0:100.0),
    )
    n_missing = Int64(round(sparsity * nrow(df_environments)))
    pb = ProgressMeter.Progress(length(environment_variables), "Simulation environmental data...")
    for (k, v) in environment_variables
        # k = "rainfall_mm_per_day"; v = environment_variables[k]
        y::Vector{Union{Missing,Float64}} = rand(v, nrow(df_environments))
        y = if n_missing == 0
            y
        else
            idx_missing = rand(1:nrow(df_environments), n_missing)
            y[idx_missing] .= missing
            y
        end
        df_environments[!, k] = y
        if verbose
            ProgressMeter.next!(pb)
        end
    end
    if verbose
        ProgressMeter.finish!(pb)
    end
    CSV.write(fname_environments, df_environments; delim = '\t')
    df_environments
end

